                                                                        {{
                                                                            item.status
                                                                                ? $t(
                                                                                      `enums.centrum.StatusUnit.${StatusUnit[item.status ?? 0]}`,
                                                                                  )
                                                                                : $t(item.name)
                                                                        }}
                                                                    </span>
                                                                </UButton>

