                                                            >
                                                                <span class="truncate">
                                                                    <span class="font-semibold"
                                                                        >{{ getOwnUnit.initials }}:</span
                                                                    >
                                                                    {{ getOwnUnit.name }}</span
                                                                >
                                                                <span class="truncate text-sm">
                                                                    <span class="font-semibold"
                                                                        >{{ $t('common.status') }}:</span
                                                                    >
                                                                    {{
                                                                        $t(
                                                                            `enums.centrum.StatusUnit.${StatusUnit[getOwnUnit.status?.status ?? 0]}`,
                                                                        )
                                                                    }}
                                                                </span>
                                                            </UButton>
                                                        </template>
                                                        <UButton
                                                            variant="soft"
