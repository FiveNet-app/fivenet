                                            <UButton
                                                :color="pendingDispatches.length > 0 ? 'red' : 'primary'"
                                                size="xl"
                                                icon="i-mdi-car-emergency"
                                                class="flex size-12 items-center justify-center"
                                                :class="[
                                                    getOwnUnit.homePostal !== undefined ? 'rounded-l-full' : 'rounded-full',
