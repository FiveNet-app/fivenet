export function convertDynamicIconNameToComponent(s: string): string {
    return (
        uppercaseFirstLetter(
            s
                .replace('i-mdi-', '')
                .toLowerCase()
                .replace(/([-_][a-z])/g, (group) => group.toUpperCase().replace('-', '').replace('_', '')),
        ) + 'Icon'
    );
}
