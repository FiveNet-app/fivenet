<script lang="ts" setup>
withDefaults(
    defineProps<{
        title?: string;
        description?: string;
        cancel?: () => Promise<any>;
        confirm: () => Promise<any>;
        icon?: string;
    }>(),
    {
        title: undefined,
        description: undefined,
        cancel: undefined,
        icon: 'i-heroicons-exclamation-circle',
    },
);

const { isOpen } = useModal();
</script>

<template>
    <UDashboardModal
        :title="title ?? $t('components.partials.confirm_dialog.title')"
        :description="description ?? $t('components.partials.confirm_dialog.description')"
        :icon="icon"
        :ui="{
            icon: { base: 'text-red-500 dark:text-red-400' } as any,
            footer: { base: 'ml-16' } as any,
        }"
        @update:model-value="cancel && cancel()"
    >
        <template #footer>
            <UButton
                color="red"
                :label="$t('common.confirm')"
                @click="
                    confirm();
                    isOpen = false;
                "
            />
            <UButton
                color="white"
                :label="$t('common.cancel')"
                @click="
                    if (cancel) {
                        cancel();
                    }
                    isOpen = false;
                "
            />
        </template>
    </UDashboardModal>
</template>
