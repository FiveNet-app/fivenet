<script lang="ts" setup>
import { z } from 'zod';
import type { FormSubmitEvent } from '#ui/types';
import ConfirmModal from '~/components/partials/ConfirmModal.vue';
import { Law } from '~~/gen/ts/resources/laws/laws';

const props = defineProps<{
    law: Law;
    startInEdit?: boolean;
}>();

const emits = defineEmits<{
    (e: 'deleted', id: string): void;
    (e: 'update:law', update: { id: string; law: Law }): void;
}>();

const { $grpc } = useNuxtApp();

const schema = z.object({
    name: z.string().min(3).max(128),
    description: z.string().min(3).max(500).optional(),
    fine: z.coerce.number().min(0).max(999_999_999).optional(),
    detentionTime: z.coerce.number().min(0).max(999_999_999).optional(),
    stvoPoints: z.coerce.number().min(0).max(999_999_999).optional(),
});

type Schema = z.output<typeof schema>;

const state = reactive<Schema>({
    name: props.law.name,
    description: props.law.description,
    fine: props.law.fine,
    detentionTime: props.law.detentionTime,
    stvoPoints: props.law.stvoPoints,
});

async function deleteLaw(id: string): Promise<void> {
    const i = parseInt(id);
    if (i < 0) {
        emits('deleted', id);
        return;
    }

    try {
        const call = $grpc.getRectorLawsClient().deleteLaw({
            id,
        });
        await call;

        emits('deleted', id);
    } catch (e) {
        $grpc.handleError(e as RpcError);
        throw e;
    }
}

async function saveLaw(lawBookId: string, id: string, values: Schema): Promise<void> {
    try {
        const call = $grpc.getRectorLawsClient().createOrUpdateLaw({
            law: {
                id: parseInt(id) < 0 ? '0' : id,
                lawbookId: lawBookId,
                name: values.name,
                description: values.description,
                fine: values.fine,
                detentionTime: values.detentionTime,
                stvoPoints: values.stvoPoints,
            },
        });
        const { response } = await call;

        emits('update:law', { id, law: response.law! });

        editing.value = false;
    } catch (e) {
        $grpc.handleError(e as RpcError);
        throw e;
    }
}

const canSubmit = ref(true);
const onSubmitThrottle = useThrottleFn(async (event: FormSubmitEvent<Schema>) => {
    canSubmit.value = false;
    await saveLaw(props.law.lawbookId, props.law.id, event.data).finally(() =>
        useTimeoutFn(() => (canSubmit.value = true), 400),
    );
}, 1000);

const modal = useModal();

const editing = ref(props.startInEdit);
</script>

<template>
    <tr v-if="!editing">
        <td class="flex flex-row py-2 pl-4 pr-3 text-sm font-medium sm:pl-1">
            <UButtonGroup class="inline-flex w-full">
                <UButton variant="link" icon="i-mdi-pencil" :title="$t('common.edit')" @click="editing = true" />
                <UButton
                    variant="link"
                    icon="i-mdi-trash-can"
                    :title="$t('common.delete')"
                    @click="
                        modal.open(ConfirmModal, {
                            confirm: async () => deleteLaw(law.id),
                        })
                    "
                />
            </UButtonGroup>
        </td>
        <td class="py-2 pl-4 pr-3 text-sm font-medium sm:pl-1">
            {{ law.name }}
        </td>
        <td class="whitespace-nowrap p-1 text-left">${{ law.fine }}</td>
        <td class="whitespace-nowrap p-1 text-left">
            {{ law.detentionTime }}
        </td>
        <td class="whitespace-nowrap p-1 text-left">
            {{ law.stvoPoints }}
        </td>
        <td class="p-1 text-left text-sm font-medium">
            {{ law.description }}
        </td>
    </tr>
    <tr v-else>
        <td class="py-2 pl-4 pr-3 text-sm font-medium sm:pl-1">
            <UButtonGroup class="inline-flex w-full">
                <UButton variant="link" icon="i-mdi-content-save" :title="$t('common.save')" @click="onSubmitThrottle" />
                <UButton
                    variant="link"
                    icon="i-mdi-cancel"
                    :title="$t('common.cancel')"
                    @click="
                        editing = false;
                        parseInt(law.id) < 0 && $emit('deleted', law.id);
                    "
                />
            </UButtonGroup>
        </td>
        <td class="py-2 pl-4 pr-3 text-sm font-medium sm:pl-1">
            <UInput
                v-model="state.name"
                name="name"
                type="text"
                :placeholder="$t('common.crime')"
                @focusin="focusTablet(true)"
                @focusout="focusTablet(false)"
            />
        </td>
        <td class="whitespace-nowrap p-1 text-left">
            <UInput name="fine" type="text" :placeholder="$t('common.fine')" :label="$t('common.fine')" />
        </td>
        <td class="whitespace-nowrap p-1 text-left">
            <UInput
                v-model="state.detentionTime"
                name="detentionTime"
                type="text"
                :placeholder="$t('common.detention_time')"
                @focusin="focusTablet(true)"
                @focusout="focusTablet(false)"
            />
        </td>
        <td class="whitespace-nowrap p-1 text-left">
            <UInput
                v-model="state.stvoPoints"
                name="stvoPoints"
                type="text"
                :placeholder="$t('common.traffic_infraction_points')"
                @focusin="focusTablet(true)"
                @focusout="focusTablet(false)"
            />
        </td>
        <td class="p-1 text-left">
            <UInput
                v-model="state.description"
                name="description"
                type="text"
                :placeholder="$t('common.description')"
                @focusin="focusTablet(true)"
                @focusout="focusTablet(false)"
            />
        </td>
    </tr>
</template>
